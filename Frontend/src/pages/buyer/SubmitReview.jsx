import { useState } from "react";
import { useNavigate } from "react-router-dom";
import Navbar from "../../components/navbar/Navbar";
import Footer from "../../components/footer/Footer";
import { createReview } from "../../api/review.api";

export default function SubmitReview() {
  const [productId, setProductId] = useState("");
  const [rating, setRating] = useState(5);
  const [comment, setComment] = useState("");
  const [message, setMessage] = useState("");
  const navigate = useNavigate();

  const submit = async (e) => {
    e.preventDefault();
    try {
      await createReview({ product_id: Number(productId), rating: Number(rating), comment });
      setMessage("Review submitted");
      setTimeout(() => navigate("/"), 700);
    } catch (err) {
      setMessage(err?.response?.data?.detail || "Failed to submit review");
    }
  };

  return (
    <div className="min-h-screen bg-gray-50">
      <Navbar />
      <div className="max-w-xl mx-auto px-4 py-8">
        <h1 className="text-2xl font-bold mb-4">Submit Review</h1>
        <form onSubmit={submit} className="bg-white rounded-xl shadow p-6 space-y-4">
          <input
            type="number"
            value={productId}
            onChange={(e) => setProductId(e.target.value)}
            className="w-full border rounded-lg px-3 py-2"
            placeholder="Product ID"
            required
          />
          <input
            type="number"
            min={1}
            max={5}
            value={rating}
            onChange={(e) => setRating(e.target.value)}
            className="w-full border rounded-lg px-3 py-2"
            placeholder="Rating 1-5"
            required
          />
          <textarea
            value={comment}
            onChange={(e) => setComment(e.target.value)}
            className="w-full border rounded-lg px-3 py-2 min-h-[110px]"
            placeholder="Comment"
          />
          <button className="w-full bg-green-600 text-white py-2 rounded-lg">Submit</button>
          {message && <p className="text-sm">{message}</p>}
        </form>
      </div>
      <Footer />
    </div>
  );
}
